<?php

function addition ($a, $b) {
  $sum= $a + $b;
  return $sum;
  }

echo addition (6, 2) ."\n";


function subtraction ($a, $b) {
  $min= $a - $b;
  return $min;
  }

echo subtraction (5, 2) ."\n";

function multiplication ($a, $b) {
  $mul= $a * $b;
  return $mul;
}

echo multiplication (8, 2) ."\n";

function division ($a, $b) {
  $div= $a / $b;
  return $div;
}

echo division (9, 3) ."\n";
